const seedDB = require('./seed');
const mongoose = require('mongoose');
const DB_URL = 'mongodb://localhost:27017/NC-news'
const data = require('./devData/index.js')

mongoose.connect(DB_URL)
  .then(() => {
    return seedDB(data)
  })
  .then(() => {
    console.log('Database successfully seeded!')
    mongoose.disconnect();
  });









































































































































  
